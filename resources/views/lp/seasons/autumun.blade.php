							</li>
							<li>
								Fill the smaller bowl with fruit juice
								<span class="ja">小さいボールにフルーツジュースを入れる。</span>
							</li>
							<li>
								Add various items such as marshmallows, gummies and jelly beans
								<span class="ja">マシュマロ、グミ、ジェリービーンズなどをジュースに入れる。</span>
							</li>
							<li>
								Add warm water to the bowl with the dry ice
								<span class="ja">ドライアイスの入っているボールにぬるま湯を入れる。</span>
							</li>
