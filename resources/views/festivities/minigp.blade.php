<div class="PaddingBandHalf"></div>
<p>
  11月に世界一決定戦とも言えるFIM MiniGP World Finalがスペイン・バレンシアのサーキットで行われ、見事総合3位の座を獲得した聖竜君！<br>
レースを終え、表彰台に立てた喜びを噛みしめていると思いきや、聖竜君の気持ちは嬉しい反面、1位を取れなかった悔しさが残っていました。<br>
日本のレースでは様々なレベルのライダーがおり、日本ではトップレベルの聖竜君ですが、ワールドシリーズで世界各国のライダーと競い、<br>
世界の壁はまだまだ高いと実感したそうです。<br>
聖竜君の今後の目標は「ワールドシリーズでは突っ込んでくるライダーに自分の走りでは対応できなかったので、臨機応変の走り方を習得し、<br>
相手がどう走ってきても対応できるよう練習していくこと」<br>
聖竜君の気持ちはすでに次回のレースに向かっていました。<br>

またスペインでの滞在中は英語を使う機会が多く、スペイン、イタリアのライダーと英語で会話しました。英語を実際使用する機会があったことで、<br>
英語に対する意識にも変化がありました。「日本で英語を話せなくて困ったことがなかったけど、海外の人は皆英語で話している事を実感し、<br>
このままだと海外の人とコミュニケーションが取れない。英語をもっと話せるようになりたい！」<br>
と意気込んでいた聖竜君、何事にも真っすぐで一生懸命な姿はすでにプロフェッショナルです。<br>
まだまだ成長する聖竜君を今後も追いかけていきます！<br>
